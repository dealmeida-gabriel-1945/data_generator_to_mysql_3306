    private void BTNAddColumnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNAddColumnActionPerformed
        if (this.comfereCamposColuna() == false) {
            if (this.confereNomeColuna() == false) {
                if (this.comparaPK() == false) {
                    Atributo at = new Atributo();
                    at.setNome(TFColunaNome.getText());
                    at.setTipo(CBTipo.getItemAt(CBTipo.getSelectedIndex()));
                    if (CBTipo.getSelectedIndex() == 0) {
                        if (TFMaxLength.getText().equals("")) {
                            at.setMaxLength(45);
                        } else {
                            at.setMaxLength(Integer.parseInt(TFMaxLength.getText()));
                        }
                    }
                    at.setPk(CBXPK.isSelected());
                    at.setNn(CBXNN.isSelected());
                    at.setAi(CBXAI.isSelected());

                    colunas.add(at);
                    this.mostraCBColunas();
                } else {
                    JOptionPane.showMessageDialog(null, "Already exists a column that is a primary key!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "This column name was already taken!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Fill in the fields!");
        }
    }//GEN-LAST:event_BTNAddColumnActionPerformed

    private void TFMaxLengthKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFMaxLengthKeyTyped
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9')
                || (c == KeyEvent.VK_BACK_SPACE)
                || (c == KeyEvent.VK_DELETE))) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_TFMaxLengthKeyTyped

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (CBColunas.getItemCount() != 0) {
            int i = JOptionPane.showConfirmDialog(null, "Do you really want to delete this column?");
            if (i == 0) {
                if (this.colunas.get(CBColunas.getSelectedIndex()).isPk() == true) {
                    this.pkEscolhido = false;
                }
                this.colunas.remove(CBColunas.getSelectedIndex());
                CBColunas.removeItemAt(CBColunas.getSelectedIndex());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void BTNEditTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNEditTableActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Do you really want to edit this table?");
        if (i == 0) {
            Tabela tal = this.tabelas.get(CBTabelas.getSelectedIndex());
            this.colunas = tal.getAtributos();
            TFTabelaNome.setText(tal.getNome());
            this.mostraCBColunas();
            this.pkEscolhido = false;
            for (Atributo col : colunas) {
                if (col.isPk() == true) {
                    this.pkEscolhido = true;
                }
            }
            this.tabelas.remove(CBTabelas.getSelectedIndex());

        }
    }//GEN-LAST:event_BTNEditTableActionPerformed

    private void BTNDeleteTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNDeleteTableActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Do you really want to delete this table?");
        if (i == 0) {
            this.tabelas.remove(CBTabelas.getSelectedIndex());
            CBTabelas.removeItemAt(CBTabelas.getSelectedIndex());
        }
    }//GEN-LAST:event_BTNDeleteTableActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (TFNome.getText().equals("") || tabelas.size() <= 0) {
            JOptionPane.showMessageDialog(null, "Fill in all fields!");
        } else {
            JFileChooser fc = new JFileChooser();
            fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            fc.setApproveButtonText("Accept");
            fc.showOpenDialog(null);
            String sql = Funcoes.geraScript(tabelas, TFNome.getText());
            String path = fc.getSelectedFile().getAbsolutePath();
            try {
                PrintWriter writer = new PrintWriter(path + "\\" + TFNome.getText() + ".sql", "UTF-8");
                writer.println(sql);
                writer.close();
                JOptionPane.showMessageDialog(null, "SQL text saved into a .sql! :D");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Something went wrong! :[ ");
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

