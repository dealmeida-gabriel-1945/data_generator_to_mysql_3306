    private void travaMaxLength() {
        if (CBTipo.getSelectedIndex() != 0) {
            TFMaxLength.setText("");
            TFMaxLength.setEditable(false);
        } else {
            TFMaxLength.setEditable(true);
        }
    }

    private void mostraCBColunas() {
        CBColunas.removeAllItems();
        for (Atributo col : colunas) {
            CBColunas.addItem(col.toString());
        }
    }

    private boolean confereNomeColuna() {
        boolean boo = false;
        for (Atributo col : colunas) {
            if (col.getNome().equals(TFColunaNome.getText())) {
                boo = true;
            }
        }
        return boo;
    }

    private boolean comparaPK() {
        if (CBXPK.isSelected() == true) {
            if (this.pkEscolhido == true) {
                return true;
            } else {
                pkEscolhido = true;
                return false;
            }
        } else {
            return false;
        }
    }

    private boolean comfereCamposColuna() {
        if (TFColunaNome.getText().equals("")) {
            return true;
        } else {
            return false;
        }
    }

    private boolean confereCamposTabela() {
        if ((TFTabelaNome.getText().equals("")) || (this.colunas.size() == 0)) {
            return true;
        } else {
            return false;
        }
    }

    private boolean confereNomeTablea() {
        boolean boo = false;
        for (Tabela tal : tabelas) {
            if (tal.getNome().equals(TFTabelaNome.getText())) {
                boo = true;
            }
        }
        return boo;
    }

    private void mostraCBTabelas() {
        for (Tabela tal : tabelas) {
            CBTabelas.addItem(tal.getNome());
        }
    }

    private void limpaCampos() {
        this.pkEscolhido = false;
        CBColunas.removeAllItems();
        TFColunaNome.setText("");
        TFMaxLength.setText("");
        CBTipo.setSelectedIndex(0);
        this.travaMaxLength();
        CBXAI.setSelected(false);
        CBXNN.setSelected(false);
        CBXPK.setSelected(false);
        TFTabelaNome.setText("");
    }
