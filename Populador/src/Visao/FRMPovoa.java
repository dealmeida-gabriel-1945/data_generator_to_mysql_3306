    private void TFQtdDePovoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFQtdDePovoKeyTyped
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9')
                || (c == KeyEvent.VK_BACK_SPACE)
                || (c == KeyEvent.VK_DELETE))) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_TFQtdDePovoKeyTyped

